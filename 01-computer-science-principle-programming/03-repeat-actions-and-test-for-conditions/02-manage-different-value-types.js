/**
 * SECTION:
 * Variables
 */

// String
let s_name = "John"; // Declare a variable named `s_name` and assign it the value "John"
let s_age = "25"; // Declare a variable named `s_age` and assign it the value "25"

// Number
let n_age = 25; // Declare a variable named `n_age` and assign it the value 25

// Boolean
let b_isMarried = false; // Declare a variable named `b_isMarried` and assign it the value false

/**
 * NOTE:
 * Dynamic Type
 * JavaScript is a dynamic type language, which means you don't have to specify the type of a variable when you define it.
 */

/**
 * BONUS:
 * Explicit Conversion
 * You can explicitly convert a variable from one type to another using the following methods:
 */

// String()
String(123); // "123" - Convert the number 123 to a string

// Number()
Number("123"); // 123 - Convert the string "123" to a number

// Boolean()
Boolean(123); // true - Convert the number 123 to a boolean (true) because it is not zero