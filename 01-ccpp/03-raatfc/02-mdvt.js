/**
 * TITLE:
 * Manage Different Value Types
 */

/**
 * SECTION:
 * Variables
 */

// String
let s_name = "John";
let s_age = "25";

// Number
let n_age = 25;

// Boolean
let b_isMarried = false;

/**
 * NOTE:
 * Dynamic Type
 * JavaScript is a dynamic type language, which means you don't have to specify the type of a variable when you define it.
 */

/**
 * BONUS:
 * Explicit Conversion
 * You can explicitly convert a variable from one type to another using the following methods:
 */

// String()
String(123); // "123"

// Number()
Number("123"); // 123

// Boolean()
Boolean(123); // true
